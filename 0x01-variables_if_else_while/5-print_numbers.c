#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - Entry point
 * Description: Print all single digit base 10 numbers
 * parameter: ch
 * Return: Always 0 (success)
 */

int main(void)

{

	int ch;

	for (ch = 0; ch < 10; ++ch)
	{
	printf("%d", ch);
	}
	printf("\n");
	return (0);

}
